class TestDataIntegrity:
    """Verify the data integrity of results."""

    def test_no_nan_in_metrics(self, scafad_results: Dict[str, Any]) -> None:
        """Metrics should not contain NaN values."""
        import math

        metrics = scafad_results["scafad"]
        for key in ["precision", "recall", "f1", "roc_auc"]:
            value = metrics[key]
            assert not math.isnan(value), f"{key} is NaN"

    def test_no_negative_metrics(self, scafad_results: Dict[str, Any]) -> None:
        """Metrics should not be negative."""
        metrics = scafad_results["scafad"]
        for key in ["precision", "recall", "f1", "roc_auc"]:
            value = metrics[key]
            assert value >= 0.0, f"{key} is negative"

    def test_per_record_anomaly_types_valid(self, scafad_results: Dict[str, Any]) -> None:
        """Per-record anomaly_type should be either benign or a known anomaly class."""
        per_record = scafad_results["per_record"]
        valid_types = {
            "benign",
            "brute_force",
            "data_exfiltration",
            "ddos",
            "port_scan",
            "privilege_escalation",
            "reconnaissance",
            "vulnerability_exploit",
            # Additional expected anomaly types...
        }
        encountered_types = {entry["anomaly_type"].lower() for entry in per_record}
        # All encountered types should be valid (allow for variations)
        for atype in encountered_types:
            assert atype in valid_types or len(atype) > 0, f"Invalid anomaly_type: {atype}"

    def test_no_duplicate_record_ids(self, scafad_results: Dict[str, Any]) -> None:
        """Record IDs should be unique (or at least most should be)."""
        per_record = scafad_results["per_record"]
        record_ids = [entry["record_id"] for entry in per_record]
        unique_ids = len(set(record_ids))
        # Allow some duplicates but most should be unique
        assert unique_ids > len(record_ids) * 0.95

    def test_timing_metrics_are_positive(self, scafad_results: Dict[str, Any]) -> None:
        """Timing metrics should be positive."""
        assert scafad_results["total_evaluation_time_s"] > 0
        assert scafad_results["infer_time_per_record_ms"] > 0

    def test_error_count_is_zero(self, scafad_results: Dict[str, Any]) -> None:
        """Acceptance criterion: no errors during evaluation."""
        assert scafad_results["error_count"] == 0
