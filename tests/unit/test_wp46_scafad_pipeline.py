class TestPerRecordResults:
    """Verify per-record structure and correctness."""

    def test_per_record_exists(self, scafad_results: Dict[str, Any]) -> None:
        """Results must have per_record list."""
        assert "per_record" in scafad_results
        assert isinstance(scafad_results["per_record"], list)

    def test_per_record_count(self, scafad_results: Dict[str, Any]) -> None:
        """Per-record list should have one entry per test record."""
        per_record = scafad_results["per_record"]
        assert len(per_record) == 6300

    def test_per_record_structure(self, scafad_results: Dict[str, Any]) -> None:
        """Each per-record entry must have required fields."""
        per_record = scafad_results["per_record"]
        required_fields = {
            "record_id",
            "anomaly_type",
            "ground_truth",
            "l4_decision",
            "l3_fused_score",
            "predicted_label",
        }
        for entry in per_record[:10]:  # Check first 10
            assert set(entry.keys()) >= required_fields

    def test_per_record_ground_truth_binary(self, scafad_results: Dict[str, Any]) -> None:
        """ground_truth must be binary (0 or 1)."""
        per_record = scafad_results["per_record"]
        for entry in per_record[:100]:  # Check sample
            gt = entry["ground_truth"]
            assert gt in (0, 1)

    def test_per_record_predicted_label_binary(self, scafad_results: Dict[str, Any]) -> None:
        """predicted_label must be binary (0 or 1)."""
        per_record = scafad_results["per_record"]
        for entry in per_record[:100]:  # Check sample
            pred = entry["predicted_label"]
            assert pred in (0, 1)

    def test_per_record_l4_decision_valid(self, scafad_results: Dict[str, Any]) -> None:
        """L4 decision must be one of the expected values."""
        per_record = scafad_results["per_record"]
        valid_decisions = {"observe", "review", "escalate", "error"}
        for entry in per_record:
            decision = entry["l4_decision"].lower()
            assert decision in valid_decisions, f"Invalid decision: {decision}"

    def test_per_record_fused_score_range(self, scafad_results: Dict[str, Any]) -> None:
        """l3_fused_score must be in [0, 1]."""
        per_record = scafad_results["per_record"]
        for entry in per_record[:100]:  # Check sample
            score = entry["l3_fused_score"]
            assert 0.0 <= score <= 1.0, f"Score {score} out of range"

    def test_decision_to_label_mapping_correct(self, scafad_results: Dict[str, Any]) -> None:
        """Verify decision→label mapping is correctly applied."""
        per_record = scafad_results["per_record"]
        mapping = {"observe": 0, "review": 1, "escalate": 1}
        for entry in per_record[:50]:  # Check sample
            decision = entry["l4_decision"].lower()
            predicted = entry["predicted_label"]
            if decision in mapping:
                expected = mapping[decision]
                assert predicted == expected, (
                    f"Decision {decision} mapped to {predicted}, expected {expected}"
                )


